import Foundation
import UIKit

func getScreenBounds() -> CGRect{
    return UIScreen.main.bounds
}

func getScreenWidth() -> CGFloat{
    return UIScreen.main.bounds.width
}

func getScreenHeight()-> CGFloat{
    return UIScreen.main.bounds.height
}
